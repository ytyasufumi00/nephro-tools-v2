def main():
    st.set_page_config(
        page_title="ICLS Instructor Complete Guide",
        page_icon="🚑",
        layout="wide"
    )

    st.title("🚑 ICLS Instructor Complete Guide")
    st.markdown("カテゴリーを選択して詳細を確認してください。")

    # ---------------------------------------------------------
    # 変更点: st.tabs ではなく st.selectbox に変更
    # これにより、スマホでも長いカテゴリー名が途切れずに表示されます。
    # ---------------------------------------------------------
    category_list = list(ICLS_DATA.keys())
    
    selected_category = st.selectbox(
        "▼ 確認したいカテゴリーをタップしてください",
        category_list,
        index=0
    )

    st.markdown("---") # 区切り線

    # ---------------------------------------------------------
    # 選択されたカテゴリーの内容を描画
    # ---------------------------------------------------------
    st.header(f"{selected_category}")
    
    items = ICLS_DATA[selected_category]
    
    # 項目ごとの詳細を展開（Expander）
    for item in items:
        with st.expander(f"📌 {item['title']}", expanded=False):
            st.markdown(item['text'])

    # フッターの余白
    st.markdown("<br><br>", unsafe_allow_html=True)

if __name__ == "__main__":
    main()
